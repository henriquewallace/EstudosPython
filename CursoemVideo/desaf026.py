n = str(input('Digite uma frase: ')).strip().lower()
print('Na frase {} a letra "a" aparece {} vezes'.format(n, n.count('a')))
print('Na frase {} a letra "a" aparece primeiro na posição {}'.format(n, n.find('a')))
print('Na frase {} a letra "a" aparece por ultimo na posição {}'.format(n, n.rfind('a')))






